void main() {
    var nilai = 100;
    var absen = 123;

    if (nilai >= 80 && absen >= 80) {
        print ('A');
    } else if (nilai >= 70 && absen >= 70) {
        print ('B');
    } else if (nilai >= 60 && absen >= 60) {
        print ('C');
    } else if (nilai >= 50 && absen >= 50) {
        print ('D');
    } else {
        print ('E');
    } 
}